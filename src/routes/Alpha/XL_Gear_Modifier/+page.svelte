	<div class="section-container">
		<h2 id="howtouse">How to Use the XL Gear Modifier Mod</h2>
		<p>
			The XLGM 2 Mod is directly accessible in-game through the 'Edit Skater' menu. Use LB/RB to
			navigate to the 'Custom Meshes' section to access your XLGH gear.
		</p>
		<h3>Adding Custom Gear</h3>
		<p>
			While the XLGM 2 Mod comes equipped with a variety of gear options, you have the freedom to
			add even more custom gear. The process is simple and allows for significant personalization.
		</p>
		<p>
			You can find custom XLGM 2 gear on skaterxl.mod.io or various Skater XL gear Discord channels.
			Each piece of XLGM 2 gear will have at least 2 files.
		</p>
		<p><strong>To add these items:</strong></p>
		<ol>
			<li>Place assets into Documents/SkaterXL/XLGearModifier/Assets</li>
			<li>Place textures into Documents/SkaterXL/Gear</li>
		</ol>
		<p>
			Remember, for each piece of custom gear, you need both the asset and its corresponding texture
			to ensure they appear correctly in the 'Custom Meshes' section in-game.
		</p>
