		<ul>
			<li>
				<strong>XL Hub:</strong> For an effortless installation experience, use XL Hub. This tool simplifies
				the process, allowing for quick and easy mod management. Download via XL Hub.
			</li>
			<li>
				<strong>Manual Installation:</strong> Download the XL Gear Mod directly from skaterxl.mod.io.
