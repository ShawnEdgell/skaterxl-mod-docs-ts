		<h1 id="xlgraphics">XL Graphics Mod</h1>
		<p>
			The XL Graphics Mod transforms Skater XL into a visually stunning skateboarding experience. It
			gives players complete control over camera settings, allowing for the creation of dynamic,
			cinematic skate videos.
		</p>
	</div>
	<div class="section-container">
		<h2 id="modinfo">Mod Information</h2>
		<ul>
			<li><strong>Authors:</strong> Babbo, Froquede</li>
			<li><strong>Working Version:</strong> 2.0.2</li>
			<li><strong>Download:</strong> Fro's Discord (#open-source-mods section)</li>
			<li><strong>Keybind to Open:</strong> Backspace</li>
		</ul>
		<p>
			<strong>Key Features:</strong> Camera Customization, Fisheye Lens Effect, Follow Camera, Community-driven
			Presets
		</p>
	</div>
	<div class="section-container">
		<h2 id="installation">Download + Installation</h2>
		<p>
			To download and install the XL Graphics Mod v2.0.2, head to Fro's Discord (#open-source-mods
			section). Make sure to grab the version that corresponds to your Skater XL version.
		</p>
		<p>
			Use UnityModManager to install: After downloading the mod, open UnityModManager and drag and
			drop the .zip file into the 'Mods' tab. Ensure Skater XL is closed during this process. Once
			installed, open Skater XL to access the mod.
		</p>
	</div>
	<div class="section-container">
		<h2 id="howtouse">How to Use the XL Graphics Mod</h2>
		<p>
			To access the mod in-game, press Backspace on your keyboard. The mod interface is divided into
			three sections: Basic, Presets, and Camera. Each section offers different customization
			options:
		</p>
		<ul>
			<li><strong>Basic:</strong> Basic graphics settings and adjustments.</li>
			<li>
				<strong>Presets:</strong> Access and apply presets created by the community or save your own.
			</li>
			<li><strong>Camera:</strong> Gameplay camera modes.</li>
		</ul>
	</div>
	<div class="section-container">
		<h2 id="followcamera">Follow Camera</h2>
		<p>
			The XL Graphics Follow Camera is an essential feature for enhancing your Skater XL experience.
			It provides a smoother gameplay experience by allowing the camera to fluidly follow the
			skater's movements. You can adjust the camera settings to your liking, creating a more
			immersive skating session. Below are some example settings to get you started, but we highly
			recommend customizing these settings to fit your personal preference.
		</p>
		<h3>Settings</h3>
		<ul>
			<li><strong>FOV:</strong> 78</li>
			<li><strong>Movement Response:</strong> 0.45</li>
			<li><strong>Rotation Response:</strong> 0.45</li>
			<li><strong>Clip Distance:</strong> 0.03</li>
			<li><strong>Right/Left:</strong> 0.36</li>
			<li><strong>Down/Up:</strong> 0.11</li>
			<li><strong>Back/Forward:</strong> -1.36</li>
		</ul>
	</div>
	<div class="section-container">
		<h2 id="fisheyepresets">Fisheye Presets</h2>
		<p>
			For fisheye settings in Skater XL, we recommend installing a custom-made preset. These
			settings are fine-tuned to replicate real-life fisheye cameras. Place XL graphic presets in
			your presets folder to install them.
		</p>
		<h3>Centennial Optics Fisheye Pack</h3>
		<p>
			To get started with the Centennial Optics Fisheye Pack, download the preset from
